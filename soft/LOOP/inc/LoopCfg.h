#ifndef _LOOP_CFG_H_
#define _LOOP_CFG_H_
/******************************************************************************/

/*******************************************************************************
*  一个扫描循环中优先扫描器件被扫描的次数，不得为0
*******************************************************************************/
#define PRIOR_SCAN_TIMES        3

/*******************************************************************************
*  一个回路上器件的最少个数，不足这个数补插发0码
*******************************************************************************/
#define MIN_DEVICE_NUM         30

/*******************************************************************************
*  火警重新扫描次数、故障重新扫描次数
*  火警、故障恢复重新扫描次数
*  重复扫描允许的结果不一致的次数,比如连扫3次只要2相同即可确认
*******************************************************************************/
#define FIRE_RESCAN_TIMES       3
#define FAULT_RESCAN_TIMES      3
#define FFRecover_RESCAN_TIMES  3
#define RESCAN_ALLOW_ERR_TIMES  1

/*******************************************************************************
*  重码、类型错误只有连续这么多次都出现才会确认
*******************************************************************************/
#define DA_TE_CNT_TIMES         2

/*******************************************************************************
* 类型错误检测间隔,注意是以s为单位,可修改
*******************************************************************************/
#define CHECK_TYPE_TIME       600

/*******************************************************************************
* 器件无响应确认的时间。
*     从检测到无响应一直到大于这个时间都一直没有响应,则确定它丢失。
*     注意是以s为单位,可修改
*******************************************************************************/
#define LOSE_REPORT_TIME       80

/*******************************************************************************
* 回路短路时间超过一个秒值将关闭回路输出。关闭时间超过另一个秒值将重新打开回路
* 来检测是否回路短路恢复。
*******************************************************************************/
#define SHORT_CLOSE_TIMES      1
#define WAIT_RECOVER_TIMERS    10
/******************************************************************************/

#define LOOP_DEV_CFG_ADDR		0x807F000

#endif

